//switch case 
const month = 3
switch (month) {
    case 1:
        console.log("jan");
        break;
    case "jan":// case of string  but this is case sensitive
        console.log("feb");
        break
    case 3:
        console.log("march"); 
        break;
//by default break execute all the remaining code below it as well 
// except default
    default:
        console.log("default case_matched");  
        break;
}
